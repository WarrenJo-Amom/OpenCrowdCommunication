package in.amant.opencrowdcommunication.model;

public class Spot {

    private String spot;
    private String distrition;

    public Spot(String spot, String distrition) {
        this.spot = spot;
        this.distrition = distrition;
    }

    public String getSpot() {
        return spot;
    }

    public String getDistrition() {
        return distrition;
    }

}
